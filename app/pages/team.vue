<script setup lang="ts">
  const { t, locale } = useI18n();

  const localeJoin = locale.value === 'zh-TW' ? '' : `/${locale.value}`;

  definePageMeta({
    title: 'team.title'
  });

  useHead({
    title: () => t('team.title')
  });

  const sharedPageTitle = useState('page-title');
  onMounted(() => {
    sharedPageTitle.value = t('team.title');
  });

  const { data: teamData, status: teamDataStatus } =
    await useLazyFetch<TeamData>('/api/team', {
      cache: 'force-cache'
    });

  // 團隊成員總數
  const totalMemberCount = computed(() => teamData.value?.totalMembers || 0);

  // 加載狀態
  const isTeamDataLoaded = computed(() => teamDataStatus.value === 'success');
</script>

<template>
  <div class="px-4 py-12">
    <!-- 主要內容 -->
    <template v-if="isTeamDataLoaded">
      <!-- 團隊組別 -->
      <TeamGroupCard
        v-for="group in teamData?.groups"
        :key="group.id"
        :group="group"
      />

      <!-- 總人數統計 -->
      <UCard class="mb-12">
        <template #header>
          <h2
            class="border-primary text-primary border-l-4 pl-3 text-2xl font-bold"
          >
            {{ t('team.total_members') }}
          </h2>
        </template>

        <p>
          <span>{{ t('team.member_count.current') }}</span>
          <span v-if="teamDataStatus === 'pending'"> ... </span>
          <span v-else-if="teamDataStatus === 'success'">
            {{ ` ${totalMemberCount} ` }}
          </span>
          <span v-else>{{ t('team.member_count.fallback') }} </span>
          <span>{{ t('team.member_count.total') }}</span>
        </p>
      </UCard>
    </template>

    <!-- 載入中骨架屏 -->
    <TeamLoadingSkeleton v-else />

    <!-- 操作按鈕 -->
    <ActionButtonsGroup>
      <template #right>
        <UButton
          :to="`${localeJoin}/join`"
          color="info"
          variant="solid"
          size="xl"
          icon="ri-link-m"
          class="animate-pop-in p-4"
        >
          {{ t('nav.join') }}
        </UButton>
      </template>
    </ActionButtonsGroup>
  </div>
</template>
